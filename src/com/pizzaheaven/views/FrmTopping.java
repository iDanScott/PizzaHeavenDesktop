    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Topping toppingToAdd = new Topping();
        toppingToAdd.setName(this.txtToppingName.getText());
        toppingToAdd.setDescription(this.txtToppingDescription.getText());
        toppingToAdd.setHeatRating(String.valueOf(this.txtHeatRating.getValue()));
        toppingToAdd.setPrice(String.valueOf(this.txtToppingPricePound.getValue())
            + "." + StringHelpers.formatPence((Integer)this.txtToppingPricePound.getValue()));
        toppingToAdd.setImage(getSelectedImage().getName());
        int apiResult;
        if (!editMode) {
            apiResult = ((ToppingsController)Session.get().getController("ToppingsController")).add(toppingToAdd);
        } else {
            apiResult = ((ToppingsController)Session.get().getController("ToppingsController")).update(toppingToAdd);
        }
        if (apiResult == 201 || apiResult == 204) {
            int result = JOptionPane.showConfirmDialog(this, "Your topping " + toppingToAdd.getName() + (apiResult == 201 ?  "  has been created" : "has been updated"), "Save Confirmation", JOptionPane.OK_CANCEL_OPTION);
            if (result == JOptionPane.OK_OPTION) {
                modified = false;
                this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Error contacting the API. Response code: " + apiResult + ". If the problem persists, please seek technical advice", "API Connectivity Error", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnSaveActionPerformed

