    public FrmStaff(Staff staff) {
        initComponents();
        DEFAULT_BORDER = txtPhoneNumber.getBorder();
        this.inputFields = new JTextComponent[]{txtPhoneNumber, txtStaffID, 
            txtPhoneNumber, txtFirstName, txtSurname, txtEmail, txtHouseNumber, 
            txtStreetName, txtCity, txtCounty, txtPostcode, txtPassword};
        initialiseCompanyPositionCombo();
        if (staff.getFirstName().contains("=")) staff = (Staff)Encryptor.decrypt(staff);
        this.setTitle("Pizza Heaven - Manager Application - Editing " + staff.getFirstName() + " " + staff.getSurname());
        editMode = true;
        txtStaffID.setText(staff.getStaffID());
        txtPhoneNumber.setText(staff.getPhoneNumber());
        txtFirstName.setText(staff.getFirstName());
        txtSurname.setText(staff.getSurname());
        txtEmail.setText(staff.getEmail());
        txtHouseNumber.setText(staff.getAddressLineOne());
        txtStreetName.setText(staff.getAddressLineTwo());
        txtCity.setText(staff.getCity());
        txtCounty.setText(staff.getCounty());
        txtPostcode.setText(staff.getPostCode());
       
        for (int i = 0; i < this.cboCompanyPosition.getItemCount(); i++) {
            CompanyPosition selectedPosition = (CompanyPosition)(Object)cboCompanyPosition.getItemAt(i);
            if (selectedPosition.getName().equals(staff.getCompanyPosition())) {
                cboCompanyPosition.setSelectedIndex(i);
            }
            
        }
        
        Date dob = new Date();
        Date start = new Date();
        try {
            dob = Globals.DATE_FORMAT.parse(staff.getDob().split("T")[0]);
            start = Globals.DATE_FORMAT.parse(staff.getStartDate().split("T")[0]);
        } catch (ParseException e) {
            System.out.println("[ERR] " + e.getMessage());
        }
        txtDobDD.setValue(dob.getDate());
        txtDobMM.setValue(dob.getMonth() + 1);
        txtDobYYYY.setValue(dob.getYear() + 1900);
        txtStartDD.setValue(start.getDate());
        txtStartMM.setValue(start.getMonth() + 1);
        txtStartYYYY.setValue(start.getYear() + 1900);
        
    }
    
    
