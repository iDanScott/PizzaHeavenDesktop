    private Boolean fieldsEmpty() {
        Boolean fieldsEmpty = false;
        for (JTextComponent inputField : inputFields) {
            if (inputField.getText() == null || inputField.getText().isEmpty()) {
                if (inputField.equals(txtPassword) && editMode) {
                   inputField.setBorder(DEFAULT_BORDER); 
                } else {
                    inputField.setBorder(BorderFactory.createLineBorder(Color.red));
                    fieldsEmpty = true;
                }
            } else {
                inputField.setBorder(DEFAULT_BORDER);
            }
        }
        return fieldsEmpty;
    }
    
    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (fieldsEmpty()) {
            JOptionPane.showMessageDialog(this, "You have some blank fields. Please correct this and try again.", "Input Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        Staff staffToAdd = new Staff();
        String salt;
        String str;
        String hash;
        String privateKey;
        
        
        if (editMode) {
            staffToAdd = ((StaffController)Session.get().getController("StaffController")).get(this.txtStaffID.getText());
            if (staffToAdd.getFirstName().contains("=")) staffToAdd = (Staff)Encryptor.decrypt(staffToAdd);
            if (staffToAdd.getPrivateKey() == null) {
                privateKey = privateKey = KeyAndSaltGenerator.SHA1(8);
            } else { 
                privateKey = staffToAdd.getPrivateKey();
            }
            if (staffToAdd.getSalt() == null) {
                salt = KeyAndSaltGenerator.SHA1(16);
            } else { 
                salt = staffToAdd.getSalt();
            }
            str = KeyStretch.keyStretch(txtPassword.getText(), salt);
            hash = Hash.SHA256(str);
        } else {
            privateKey = KeyAndSaltGenerator.SHA1(8);
            salt = KeyAndSaltGenerator.SHA1(16);
            str = KeyStretch.keyStretch(txtPassword.getText(), salt);
            hash = Hash.SHA256(str);
        }
        
        staffToAdd.setStaffID(txtStaffID.getText());
        staffToAdd.setAddressLineOne(txtHouseNumber.getText());
        staffToAdd.setAddressLineTwo(txtStreetName.getText());
        staffToAdd.setEmail(txtEmail.getText());
        staffToAdd.setCity(txtCity.getText());
        if (txtPassword.getText().isEmpty()) 
            staffToAdd.setPassword(staffToAdd.getPassword());
        else
            staffToAdd.setPassword(hash);
        staffToAdd.setCompanyPosition(getSelectedCompanyPosition().getName());
        staffToAdd.setCounty(txtCounty.getText());
        staffToAdd.setFirstName(txtFirstName.getText());
        staffToAdd.setDob(Globals.DATE_FORMAT.format(new Date((Integer)this.txtDobYYYY.getValue() - 1900, (Integer)this.txtDobMM.getValue() - 1, (Integer)this.txtDobDD.getValue())));
        staffToAdd.setPhoneNumber(txtPhoneNumber.getText());
        staffToAdd.setStartDate(Globals.DATE_FORMAT.format(new Date((Integer)this.txtStartYYYY.getValue() - 1900, (Integer)this.txtStartMM.getValue() - 1, (Integer)this.txtStartDD.getValue())));
        staffToAdd.setPostCode(txtPostcode.getText());
        staffToAdd.setSurname(txtSurname.getText());
        staffToAdd.setPrivateKey(privateKey);
        staffToAdd.setSalt(salt);
        staffToAdd.setEmployed("1");
        staffToAdd = (Staff)Encryptor.encrypt(staffToAdd);
        
        int apiResult;
        
        if (!editMode) {
            apiResult = ((StaffController)Session.get().getController("StaffController")).add(staffToAdd);
        } else {
            apiResult = ((StaffController)Session.get().getController("StaffController")).update(staffToAdd);
        }
        if (apiResult == 201 || apiResult == 204) {
            JOptionPane.showMessageDialog(this, "Your Staff Member " + txtFirstName.getText() + " " + txtSurname.getText() + (apiResult == 201 ? " has been saved" : " has been updated"), "Save Confirmation", JOptionPane.OK_OPTION);
            modified = false;
            this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(this, "Error contacting the API. Response code: " + apiResult + ". If the problem persists, please seek technical advice", "API Connectivity Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

